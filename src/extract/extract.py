import requests

def extrair_dados_crypto():

    url = "https://api.coingecko.com/api/v3/coins/markets"

    params = {
        "vs_currency": "usd",
        "order": "market_cap_desc",
        "per_page": 10,
        "page": 1
    }

    response = requests.get(url, params=params)
    data = response.json()

    return data



if __name__ == "__main__":
    data = extrair_dados_crypto()
